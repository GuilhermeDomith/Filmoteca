package ti.lpv.filmoteca.modelo;

public class Pais {

	private Long codigoPais;
	private String nome;
	
	public Pais() {
		super();
	}
	
	public Long getCodigoPais() {
		return codigoPais;
	}
	public void setCodigoPais(Long codigoPais) {
		this.codigoPais = codigoPais;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
}
